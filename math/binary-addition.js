function addBinary(a,b) {
  return (a + b).toString(2)
}

addBinary(1, 1) // '10'
addBinary(1, 2) // '11'
addBinary(4, 5) // '1001'
addBinary(8, 20) // '11100'
addBinary(100, 20) // '1111000'
addBinary(40, 50) // '1011010'
addBinary(65, 77) // '10001110'
addBinary(40, 50) // '1011010'
addBinary(1, 0) // '1'
