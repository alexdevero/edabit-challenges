function convert(deg) {
	return deg.indexOf('°C') === -1 && deg.indexOf('°F') === -1 ? 'Error' : deg.indexOf('°C') > -1 ? Math.round(parseInt(deg.substring(0, deg.indexOf('°C'))) * 1.8 + 32) + '°F' : Math.round((parseInt(deg.substring(0, deg.indexOf('°F'))) - 32) / 1.8) + '°C'
}

