function neutralise(s1, s2) {
	return [...s1].map((el, i) => el === [...s2][i] ? el : 0).join('')
}

neutralise("--++--", "++--++") // "000000"
neutralise("-+-+-+", "-+-+-+") // "-+-+-+"
neutralise("-++-", "-+-+") // "-+00"
neutralise("--++", "++++") // "00++"
neutralise("+++--+---", "++----++-") // "++0--000-"
neutralise("-----", "-----") // "-----"
neutralise("-+", "++") // "0+"
neutralise("--", "-+") // "-0"
neutralise("-++", "+--") // "000"
neutralise("++-++--++-", "-+++-++-++") // "0+0+0000+0"
neutralise("-++-+-++-", "+-++++---") // "00+0+000-"
neutralise("---++-+--", "-+++--++-") // "-00+0-+0-"
neutralise("+-----+++-", "--+-+-++--") // "0-0-0-++0-"
neutralise("+-----+-", "---++-++") // "0--00-+0"
neutralise("-+--+-+---", "-+--+-+-+-") // "-+--+-+-0-"
neutralise("+-+", "-++") // "00+"
neutralise("-++", "-+-") // "-+0"
neutralise("---+", "-+++") // "-00+"
neutralise("+--", "+--") // "+--"
neutralise("--+++-+-", "+++++---") // "00+++-0-"
