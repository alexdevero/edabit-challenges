function countSyllables(str) {
	return str.length / 2
}

countSyllables("Hehehehehehe") // 6
countSyllables("bobobobobobobobo") // 8
countSyllables("NANANANA") // 4
countSyllables("lelelele") // 4
countSyllables("momomomomomomomomo") // 9
countSyllables("WiWiWiWiWiWiWiWiWiWi") // 10
countSyllables("RURURURURURUrurururuRURU") // 12
countSyllables("go") // 1
countSyllables("dede") // 2
