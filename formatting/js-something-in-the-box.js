function inBox(arr) {
	return arr.some(el => el.search(/\s*\*\s*/) > -1)
}

inBox(["###", "# #", "###"]) // false
inBox(["####", "#  #", "#  #", "####"]) // false
inBox(["#####", "#   #", "#   #", "#   #", "#####"]) // false
inBox(["###", "#*#", "###"]) // true
inBox(["####", "# *#", "#  #", "####"]) // true
inBox(["#####", "#  *#", "#   #", "#   #", "#####"]) // true
inBox(["#####", "#   #", "# * #", "#   #", "#####"]) // true
inBox(["#####", "#   #", "#   #", "# * #", "#####"]) // true
inBox(["#####", "#*  #", "#   #", "#   #", "#####"]) // true
