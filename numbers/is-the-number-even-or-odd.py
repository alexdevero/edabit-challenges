def isEvenOrOdd(num):
	if num % 2 == 0:
		return 'even'
	else:
		return 'odd'

isEvenOrOdd(3) # "odd"
isEvenOrOdd(0) # "even"
isEvenOrOdd(7) # "odd"
isEvenOrOdd(12) # "even"
isEvenOrOdd(6474) # "even"
isEvenOrOdd(3) # "odd"
isEvenOrOdd(301) # "odd"
isEvenOrOdd(-3) # "odd"
isEvenOrOdd(-0) # "even"
isEvenOrOdd(-7) # "odd"
isEvenOrOdd(-12) # "even"
isEvenOrOdd(-6474) # "even"
isEvenOrOdd(-3) # "odd"
isEvenOrOdd(-301) # "odd"