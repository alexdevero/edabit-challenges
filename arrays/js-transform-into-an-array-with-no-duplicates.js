function set(arr) {
	return [...new Set(arr)]
}

set([1, 3, 3, 5, 5]) // [1, 3, 5]
set([4, 4, 4, 4]) // [4]
set([5, 7, 8, 9, 10, 15]) // [5, 7, 8, 9, 10, 15]
set([5, 9, 9]) // [5, 9]
set([1, 2, 3, 4, 5, 5, 6, 6, 7]) // [1, 2, 3, 4, 5, 6, 7]
set([1, 1, 2, 2, 2]) // [1, 2]
set(['A', 'A', 'A', 'A']) // ['A']
set(['A', 'B', 'C', 'D']) // ['A', 'B', 'C', 'D']
