function sortIt(arr) {
	return arr.sort((x, y) => x - y)
}

sortIt([4, 1, 3]) // [1, 3, 4]
sortIt([[4], [1], [3]]) // [[1], [3], [4]]
sortIt([4, [1], 3]) // [[1], 3, 4]
sortIt([[4], 1, [3]]) // [1, [3], [4]]
sortIt([[3], 4, [2], [5], 1, 6]) // [1, [2], [3], 4, [5], 6]
sortIt([[3], 7, [9], [5], 1, 6]) // [1, [3], [5], 6, 7, [9]]
sortIt([[3], 7, [9], [5], 1, 6, [0]]) // [[0], 1, [3], [5], 6, 7, [9]]
